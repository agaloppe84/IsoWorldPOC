import EngineCore

enum UILabelRenderer {
    static func commands(
        text: String,
        originX: Float,
        originY: Float,
        pixelHeight: Float,
        color: UIStyleColor,
        layer: Int
    ) -> [UIDrawCommand] {
        let cell = max((pixelHeight / 7).rounded(.down), 1)
        let characterWidth = cell * 5
        let characterAdvance = characterWidth + cell
        let normalizedText = text.uppercased()
        var commands: [UIDrawCommand] = []

        for (characterIndex, character) in normalizedText.enumerated() {
            guard character != " " else {
                continue
            }

            let pattern = glyphPattern(for: character)
            let glyphX = originX + Float(characterIndex) * characterAdvance

            for (rowIndex, row) in pattern.enumerated() {
                for (columnIndex, cellValue) in row.enumerated() where cellValue == "1" {
                    commands.append(UIDrawCommand(
                        kind: .glyph,
                        rect: UIFrameRect(
                            x: glyphX + Float(columnIndex) * cell,
                            y: originY + Float(rowIndex) * cell,
                            width: cell,
                            height: cell
                        ),
                        color: color,
                        layer: layer,
                        materialOrder: 4
                    ))
                }
            }
        }

        return commands
    }

    static func measuredWidth(text: String, pixelHeight: Float) -> Float {
        let cell = max((pixelHeight / 7).rounded(.down), 1)
        let characterAdvance = cell * 6
        return Float(text.count) * characterAdvance
    }

